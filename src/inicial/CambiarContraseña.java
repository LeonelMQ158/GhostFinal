    private void cambiarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cambiarMousePressed
        for(int i = 0; i < login.usuarioArray.length; i++){
            if(login.contraseña.equalsIgnoreCase(login.contraseñaArray[i])){
                contra.getText();
                nuevacont.getText();
                login.contraseñaArray[i] = nuevacont.getText();
                camb.setText("Cambio de contraseña exitoso");
            }
        }    
    }//GEN-LAST:event_cambiarMousePressed

